''' Number Python '''
# 3 Type Numerik
'''
int (bilangan bulat), float (bilangan mengambang), complex (bilangan imajiner(j))
'''
# example
x = 20
print(type(x))

print('\n')

y = 20.0
print(type(y))

print('\n')

z = 20j
print(type(z))

print('\n')

